# Faça um Programa que leia um vetor de 10 caracteres, e diga quantas consoantes foram lidas. Imprima as consoantes.

import os


os.system('cls')

lista_caracteres = []
lista_consoantes = []

for letter in range(1,11):
    let = input(f'Insira a {letter}ª letra: ')
    lista_caracteres.append(let)
    if let != 'a' and let != 'e' and let != 'i' and let != 'o' and let != 'u':
        lista_consoantes.append(let)
        quantidade = len(lista_consoantes)
    else:
        continue

print('Lista fornecida:', lista_caracteres)
print('Lista de consoantes:', lista_consoantes)
print('Quantidade de consoantes:', quantidade)
        
    
